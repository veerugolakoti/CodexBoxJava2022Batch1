package JavaTraining.javaassessment;

public class Operation {
    public int Add (int a, int b ){
        return a+b;
    }
    public double mul(double c,double d){
        return c*d;
    }
    public long sub(long g,long h){
        return g-h;
    }
    public float div(float e,float f){
        return e/f;
    }


}
