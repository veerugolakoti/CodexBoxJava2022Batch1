package sangamesh.datatypes;
public class Operators {
    public static void main(String[] args) {
        //arithmetic
        int a=3,b=4;
        System.out.println(""+(a+b));
        System.out.println(""+(a-b));
        System.out.println(""+(a%b));
        System.out.println(""+(a/b));
        System.out.println(""+(a*b));
        //assignment
        int c=24;
        System.out.println(""+c);
        //relation
        System.out.println(""+(a>b));
        System.out.println(""+(a<b));
        System.out.println(""+(a<=b));
        System.out.println(""+(a>=b));
        System.out.println(""+(a==b));
        System.out.println(""+(a!=b));
        //unary
        System.out.println(""+(++a));
        System.out.println(""+(a++));
        System.out.println(""+(a--));
        System.out.println(""+(--a));
        //logical
        System.out.println(""+(a&b));
        System.out.println(""+(a|b));
        System.out.println(""+(a!=b));
    }
}
