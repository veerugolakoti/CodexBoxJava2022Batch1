package sangamesh.condition;
public class Switchstatement {
    public static void main(String[] args) {
        int a = 2,b=8,c=9;

        switch (a) {
            case 8:
                System.out.println(a);
                break;
            case 2:
                System.out.println(b);
            case 5:
                System.out.println(c);


        }
    }
}