package Veeru.sample.inheritance;

public class Shine  {
   private String engineCpacity;
    private Integer rpm;
    private Integer fuelTankCapaciy;
    private  String mileage;

    public String getEngineCpacity() {
        return engineCpacity;
    }

    public void setEngineCpacity(String engineCpacity) {
        this.engineCpacity = engineCpacity;
    }

    public Integer getRpm() {
        return rpm;
    }

    public void setRpm(Integer rpm) {
        this.rpm = rpm;
    }

    public Integer getFuelTankCapaciy() {
        return fuelTankCapaciy;
    }

    public void setFuelTankCapaciy(Integer fuelTankCapaciy) {
        this.fuelTankCapaciy = fuelTankCapaciy;
    }

    public String getMileage() {
        return mileage;
    }

    public void setMileage(String mileage) {
        this.mileage = mileage;
    }
}
